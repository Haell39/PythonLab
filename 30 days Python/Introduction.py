
# Numerical Operations

print(2 + 3)  # Addition
print(5 - 2)  # Subtraction
print(3 * 4)  # Multiplication
print(10 / 2)  # Division
print(10 // 3)  # Floor Division
print(10 % 3)  # Modulus
print(2 ** 3)  # Exponentiation

# Data types

print(type(10))  # Integer
print(type(3.14))  # Float
print(type(1 + 3j))  # Complex
print(type('Rael Python'))  # String
print(type([1, 2, 3]))  # List
print(type({'Name':'Andrade'}))  # Dictionary
print(type({4.5, 7.8, 9.7}))  # Set
print(type((9.8, 7.4, 1.2)))  # Tuple
print(type(3 == 5))  # Boolean
print(type(5 >= 5))  # Boolean OBS: -- => -- is wrong syntax, correct is -- >= --
