# Escape Characters:

'''
To print a backslash, use two backslashes: \\ and a tab: \t
\n gonna start a new line
'''

print("Hello, World!\nMy name is James Web \\ and I am 20 years old.\t aqui ta o tab")

'''
To print double quotes: \" and single quotes: \'
'''

print("Hello, World! My name is \"James Web\" and I am \'20\' years old" )

#OR Just write:

print('Hello, World! My name is "James Web" and I am "20" years old')

# OR:

print("Hello, World! My name is \"James Web and I am \'20 years old")
